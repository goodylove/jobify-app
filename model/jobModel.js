import mongoose from "mongoose";

const JobScheme = new mongoose.Schema(
  {
    company: String,
    position: String,
    jobStatus: {
      type: String,
      enum: ["interview", "declined", "pending"],
      default: "pending",
    },
    jobType: {
      type: String,
      enum: ["full-time", "part-time", "internship"],
      default: "full-time",
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("Jobs", JobScheme);
